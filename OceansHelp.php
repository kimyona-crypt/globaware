                        <div class="act">
                      
                      <p>Healthy oceans play a vital role in sustaining life on our planet, by providing a source of food for billions of people, and helping to regulate the climate. But this week the Intergovernmental Panel on Climate Change (IPCC) released a special report containing stark scientific evidence that confirms our worst fears – the climate crisis is an oceans crisis.</p>
                      <p>The CO2 we’re pumping into the atmosphere by burning fossil fuels is warming our planet. This is causing ice sheets and glaciers to melt at an alarming rate and sea levels to rise,  threatening to displace millions. 
                        <br><br>
                        This CO2 is also being absorbed by seawater. This is bad news; it makes the ocean more acidic and harms unique ecosystems, like beautiful warm-water coral reefs.
                        <br><br>
                        Why does this matter? Marine life can help us; it naturally absorbs and stores carbon, locking it away in the ocean. Plants in coastal areas, such as mangrove forests and seagrass meadows, trap carbon in underwater sediment and soil. And sea creatures capture it in the food chain,  most of which eventually sinks to the deep ocean seabed in the bodies of dead animals. 
                        <br><br>
                        We can’t rely on this natural process to tackle climate change alone – we need to halt carbon emissions to; but it’s vital that we protect areas where carbon is stored, and the wider ocean, or the climate crisis will become even worse.</p>
                      <p>Beyond the threats of the climate emergency outlined by the IPCC report, the ongoing impact of destructive human activities such as overfishing, offshore oil and gas drilling and plastic pollution is causing further devastation, affecting our oceans’ ability to cope with and soften the blow of the climate breakdown.
                        <br><br>
                        Thankfully, among all the bad news, we have a huge opportunity to protect the oceans. Governments are currently negotiating a Global Ocean Treaty at the UN. If a strong treaty is agreed, it could pave the way for a network of ocean sanctuaries, placing at least 30% of our oceans off-limits to human activity by 2030. 
                        <br><br>
                        Such widespread protection would have a profound impact on ocean health, helping marine life recover from human pressures and thrive. Healthy, resilient oceans will continue to slow climate breakdown and support life on earth.</p>
                        <p>Last month governments completed the penultimate round of treaty negotiations. Although many countries are supportive of the process, to few are displaying enough ambition to secure an agreement that will really count. 
                            <br><br>
                            Today’s report is a wake-up call for those governments who continue to sleepwalk through the climate crisis. Governments need to prioritise the treaty negotiations, and demonstrate this by sending Heads of State or senior ministers to New York next year to personally secure a bold Global Ocean Treaty.
                            <br><br>
                            At the same time, political and business leaders need to address the root cause of the oceans crisis. Efforts to transform energy systems away from polluting fossil fuels to 100% renewable energy must accelerate, and deforestation has to stop. Reducing carbon emissions to zero will ease climate pressure on our oceans, helping the benefits of a network of ocean sanctuaries to be fully realised, allowing marine life to get back to health.</p>
